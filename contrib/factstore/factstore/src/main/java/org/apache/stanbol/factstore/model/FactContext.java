package org.apache.stanbol.factstore.model;

import java.util.Date;

public class FactContext {

    private int id;
    private Date created;
    private Date updated;
    private Date validFrom;
    private Date vaildTo;
    private String contextURN;

    public Date getCreated() {
        return created;
    }

    public void setCreated(Date created) {
        this.created = created;
    }

    public Date getUpdated() {
        return updated;
    }

    public void setUpdated(Date updated) {
        this.updated = updated;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getValidFrom() {
        return validFrom;
    }

    public void setValidFrom(Date validFrom) {
        this.validFrom = validFrom;
    }

    public Date getVaildTo() {
        return vaildTo;
    }

    public void setVaildTo(Date vaildTo) {
        this.vaildTo = vaildTo;
    }

    public String getContextURN() {
        return contextURN;
    }

    public void setContextURN(String contextURN) {
        this.contextURN = contextURN;
    }

}
